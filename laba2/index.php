//Ассоциативные массивы. Дан массив с элементами 1, 2, 3, 4, 5. С помощью цикла foreach 
// найдите сумму элементов этого массива. Запишите ее в переменную $result.

$initialArr = [1,2,3,4,5];//Создаем изначальный массив
function findSum($arr){//обявляем функцию findSum в которую передаем массив
    $result = 0;//создаем переменную которая будет записывать сумму элементов
    foreach( $arr as $num){//циклом прохожимся по массиву прибавляя каждый элемент к переменной $result
