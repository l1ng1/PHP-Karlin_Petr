//Двумерный массив. Дан трехмерный массив с числами, например 
// [[[1, 2], [3, 4]], [[5, 6], [7, 8]]]. Найдите сумму элементов этого массива.
// Массив, конечно же, может быть произвольным.

$dumericArr = [[[1, 2], [3, 4]], [[5, 6], [7, 8]]];


function findDumericSum($arr){
    $result = [];
    array_walk_recursive($arr,function($elem) use (&$result){
        $result[] = $elem;
    });
    return $result;
}

findSum(findDumericSum($dumericArr));









